 private void connection (){
 connexion c = new connexion();
    String query = "SELECT name, password FROM users WHERE id=?";
    try {
        PreparedStatement pstmt = c.con.prepareStatement(query);
        pstmt.setInt(1, userid);

        ResultSet res = pstmt.executeQuery();
        if (res.next()) { 
            motdepasse = res.getString("password");
            nom = res.getString("name");
        } else {
         
            JOptionPane.showMessageDialog(this, "Utilisateur non trouvé", "Erreur", JOptionPane.ERROR_MESSAGE);
        }

        
        res.close();
        pstmt.close();
        c.con.close();
    } catch (SQLException ex) {
        Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
      
        JOptionPane.showMessageDialog(this, "Une erreur est survenue lors de la récupération des données", "Erreur", JOptionPane.ERROR_MESSAGE);
    }
 
 
 }
