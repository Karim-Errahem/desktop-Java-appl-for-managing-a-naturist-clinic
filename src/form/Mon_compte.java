    private void modifierpasseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_modifierpasseActionPerformed
   String Name = nom_p.getText();
    String pass = String.valueOf(passea.getPassword());
    String passn = String.valueOf(passen.getPassword());
    
    if (Name.equals(nom) && pass.equals(motdepasse)) {
        connexion co = new connexion();
        String query = "UPDATE users SET password = ? WHERE id = ?";
        try {
            PreparedStatement pstmt1 = co.con.prepareStatement(query);
            pstmt1.setString(1, passn); // Définir le nouveau mot de passe
            pstmt1.setInt(2, userid);    // Définir l'ID de l'utilisateur

            int rowsAffected = pstmt1.executeUpdate();
            if (rowsAffected > 0) {
                // Mot de passe mis à jour avec succès
                JOptionPane.showMessageDialog(this, "Mot de passe mis à jour avec succès");
                nom_p.setText("");
                passea.setText("");
                passen.setText("");
               this.motdepasse=passn;
            } else {
                
                JOptionPane.showMessageDialog(this, "Échec de la mise à jour du mot de passe");
                 nom_p.setText("");
                passea.setText("");
                passen.setText("");
            }
        } catch (SQLException ex) {
            Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, "Une erreur est survenue lors de la mise à jour du mot de passe", "Erreur", JOptionPane.ERROR_MESSAGE);
        } 
    } else {
       
        JOptionPane.showMessageDialog(this, "Nom d'utilisateur ou mot de passe incorrect");
         nom_p.setText("");
                passea.setText("");
                passen.setText("");
    }
    }//GEN-LAST:event_modifierpasseActionPerformed

